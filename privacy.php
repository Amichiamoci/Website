				<h2>Privacy</h2>
				<p>
	                La presente costituisce la nostra informativa sulla privacy,
	                che illustra chi siamo, i motivi e le modalit&agrave; del
	                trattamento dei dati personali, i suoi diritti e i riferimenti
	                per contattarci.

	                <strong>Chi siamo</strong>

	                Siamo "Amichiamoci" e i nostri contatti e altri recapiti sono
	                indicati nella sezione contatti di questo sito web.
	                Amichiamoci &egrave; il titolare del trattamento dei dati
	                personali trattati in conformit&agrave; alla presente
	                informativa.

	                <strong>I dati personali oggetto di trattamento</strong>

	                Possiamo effettuare il trattamento di dati personali che la
	                riguardano qualora lei sia una persona la quale intende
	                contattarci per ricevere informazioni relative alla nostra
	                attivit&agrave;. In tal caso, potremmo aver ottenuto i suoi
	                recapiti direttamente da lei (ad esempio: attraverso il sito
	                web, inoltrati per mail o da altra fonte).

	                <strong>Tipi di dati personali oggetto di trattamento</strong>

	                Tra i dati personali da noi trattati sono comprese le
	                informazioni di seguito indicate:<br><br>

	                    1) I suoi dati anagrafici (nome, cognome, data e luogo
	                    di nascita...)<br>
	                    2) Il suo indirizzo e-mail o altro recapito che sia
	                    ritenuto necessario per la natura del nostro rapporto.

	                <strong>
	                    I dati personali che trattiamo quando utilizza il nostro
	                    sito web
	                </strong>

	                Le informazioni ottenute da lei quando utilizza il
	                nostro sito web sono le seguenti:<br><br>

	                    1) Dati di navigazione: i sistemi informatici e le
	                    procedure software preposte al funzionamento di questo
	                    sito web acquisiscono, nel corso del loro normale
	                    esercizio, alcuni dati personali la cui trasmissione
	                    &agrave; implicita nell'uso dei protocolli di
	                    comunicazione di Internet. Si tratta di informazioni che
	                    non sono raccolte per essere associate a interessati
	                    identificati, ma che per loro stessa natura potrebbero,
	                    attraverso elaborazioni ed associazioni con dati detenuti
	                    da terzi, permettere di identificare gli utenti. In questa
	                    categoria di dati rientrano gli indirizzi IP o i nomi a
	                    dominio dei computer utilizzati dagli utenti che si
	                    connettono al sito, gli indirizzi in notazione URI
	                    (Uniform Resource Identifier) delle risorse richieste,
	                    l'orario della richiesta, il metodo utilizzato nel
	                    sottoporre la richiesta al server, la dimensione del file
	                    ottenuto in risposta, il codice numerico indicante lo
	                    stato della risposta data dal server (buon fine, errore,
	                    ecc.) ed altri parametri relativi al sistema operativo e
	                    all'ambiente informatico dell'utente.<br><br>
	                    2) Dati forniti volontariamente dall'utente: l'invio
	                    facoltativo, esplicito e volontario di posta elettronica
	                    all'indirizzo indicato su questo sito comporta la
	                    successiva acquisizione dell'indirizzo del mittente,
	                    necessario per rispondere alle richieste, nonch&egrave;
	                    degli eventuali altri dati personali inseriti nella
	                    missiva.

	                <strong>Facoltativit&agrave; del conferimento dei dati</strong>

	                A parte quanto specificato per i dati di navigazione, l'utente
	                &egrave; libero di fornire i dati personali riportati nel
	                modulo contenuto nella sezione contatti di questo sito web. Il
	                conferimento dei suoi dati personali &egrave; necessario per
	                fornirle le informazioni e i servizi da lei richiesti; la
	                mancata comunicazione dei suoi dati personali comporter&agrave;
	                la nostra impossibilit&agrave; di fornirle quanto richiestoci.

	                <strong>Utilizzo dei suoi dati personali</strong>

	                I suoi dati, verranno trattati in forma informatizzata e, per
	                la loro protezione, abbiamo adottato protezioni adeguate in
	                conformit&agrave; con la normativa.

	                <strong>
	                    Base giuridica del trattamento dei suoi dati personali da
	                    noi svolto
	                </strong>

	                Nel caso in cui, per esservi autorizzati, sia necessario
	                ottenere anticipatamente il suo consenso al trattamento,
	                otterremo e ci fonderemo sul suo consenso rispetto al
	                trattamento in oggetto (si veda di seguito come revocare il
	                proprio consenso in ogni momento). Altrimenti, tratteremo i
	                suoi dati personali, per l'espletamento e le finalit&agrave;
	                contrattuali.

	                <strong>Luogo in cui trattiamo i dati personali</strong>

	                I dati da noi trattati a lei riferiti sono conservati su un
	                web server situato presso la sede dell'Azienda che ci fornisce
	                il servizio di hosting per il nostro sito web, cos&igrave;
	                come i trattamenti connessi ai servizi web del sito. Nessun
	                dato derivante dal servizio web viene comunicato o diffuso. I
	                dati personali forniti dagli utenti che inoltrano richieste di
	                invio di materiale informativo sono utilizzati al solo fine di
	                eseguire il servizio o la prestazione richiesta.

	                <strong class="italic">
	                    Tutti i dati personali da noi trattati sono conservati in
	                    modo sicuro
	                </strong>

	                <strong>Durata del trattamento dei dati personali</strong>

	                Effettuiamo il trattamento dei dati personali solo per il
	                tempo necessario al(i) fine(i) per il(i) quale(i) sono stati
	                originariamente raccolti, successivamente al(i) quale(i)
	                saranno cancellati o archiviati, eccetto nella misura in cui
	                il loro trattamento continui ad essere per noi necessario al
	                fine di osservare obblighi di legge cui siamo sottoposti o per
	                ogni altro fine lecito e legittimo.

	                <strong>I suoi diritti</strong>

	                In relazione ai suoi dati personali da noi trattati, lei ha i
	                seguenti diritti:<br><br>

	                    1) Pu&ograve; richiedere l’accesso ai relativi dati
	                    personali (la preghiamo di consultare la successiva
	                    sezione per vedere come ottenere l’accesso ai suoi dati
	                    personali).<br>
	                    2) Pu&ograve; chiedere la rettifica di dati personali
	                    oggetto di trattamento che presentano errori.<br><br>

	                In alcune circostanze (normalmente quando i dati personali
	                sono stati da lei forniti e non sia per noi pi&ugrave;
	                necessario effettuarne il trattamento), pu&ograve; chiederne
	                la cancellazione. Laddove trattiamo dati personali a lei
	                relativi sulla base del consenso accordato prima del
	                trattamento, pu&ograve; revocare il suo consenso in qualunque
	                momento e, a seguito di tale revoca, metteremo fine al
	                trattamento in oggetto. Pu&ograve; opporsi al trattamento
	                svolto sulla base dei nostri interessi legittimi. In caso di
	                reclamo su un qualsiasi trattamento dei suoi dati personali da
	                noi effettuato, pu&ograve; contattarci o depositare un reclamo
	                formale presso il Garante per la protezione dei dati personali.

	                <strong>Come revocare il suo consenso al trattamento</strong>

	                Pu&ograve; revocare il suo consenso a ogni relativo
	                trattamento di dati personali:<br><br>

	                    1) Inviandoci una e-mail al seguente indirizzo:
	                    <a href="mailto:info@amichiamoci.it">
	                        info@amichiamoci.it
	                    </a>

	                <strong>
	                    Come esercitare il diritto di accesso ai suoi dati
	                    personali
	                </strong>

	                Pu&ograve; esercitare il suo diritto di accesso ai dati
	                personali:<br><br>

	                    1) Inviandoci una e-mail al seguente indirizzo:
	                    <a href="mailto:info@amichiamoci.it">
	                        info@amichiamoci.it
	                    </a><br><br>

	                La preghiamo di notare che le potrebbero essere richieste
	                ulteriori informazioni per confermare la sua identit&agrave;
	                prima di fornirle le informazioni desiderate.

	                <strong>
	                    Il Garante per la protezione dei dati personali
	                </strong>

	                Il Garante per la protezione dei dati personali &egrave;
	                l’autorit&agrave; di vigilanza in Italia. Il Garante pu&ograve;
